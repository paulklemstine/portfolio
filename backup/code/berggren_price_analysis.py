def is_gauss_prime(a, b):
    """Check if a + bi is a Gaussian prime."""
    if a == 0: return abs(b) % 4 == 3 and isprime(abs(b))
    if b == 0: return abs(a) % 4 == 3 and isprime(abs(a))
    return isprime(a*a + b*b)

def gauss_prime_tree(root, max_depth=8, max_nodes=5000):
    """Build tree of Gaussian primes using multiplication by small Gaussian primes.
    Also tries addition-based branching for broader coverage."""
    tree = {root: (0, None)}
    alive = defaultdict(int)
    alive[0] = 1

    # Use both multiplication and offset operations
    def children_of(a, b):
        """Generate candidate Gaussian integers from (a+bi)."""
        results = []
        # Multiply by small Gaussian primes
        for ma, mb in [(1, 1), (2, 1), (1, 2), (3, 2)]:
            ca = a*ma - b*mb
            cb = a*mb + b*ma
            results.append((ca, cb))
        # Add small Gaussian primes
        for da, db in [(2, 1), (1, 2), (-1, 2), (2, -1), (0, 1), (1, 0)]:
            results.append((a + da, b + db))
        return results

    queue = [(root, 0)]
    while queue and len(tree) < max_nodes:
        z, depth = queue.pop(0)
        if depth >= max_depth:
            continue
        a, b = z

        for ca, cb in children_of(a, b):
            if abs(ca) > 10**6 or abs(cb) > 10**6:
                continue
            # Canonical form: both positive, sorted
            ca2, cb2 = abs(ca), abs(cb)
            key = (min(ca2, cb2), max(ca2, cb2))
            if key[0] == 0 and key[1] == 0:
                continue
            if is_gauss_prime(ca, cb) and key not in tree:
                tree[key] = (depth+1, z)
                queue.append(((ca2, cb2), depth+1))
                alive[depth+1] += 1

    return tree, alive


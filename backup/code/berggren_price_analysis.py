B_B_inv = np.array([[ 1,  2, -2], [ 2,  1, -2], [-2, -2,  3]], dtype=np.int64)
B_C_inv = np.array([[-1, -2,  2], [ 2,  1, -2], [-2, -2,  3]], dtype=np.int64)


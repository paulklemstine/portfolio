def sb_prime_tree(max_depth=14, max_nodes=10000):
    """Binary tree: (lo, hi) -> (lo, med), (med, hi) where med = nextprime(lo+hi-1)."""
    tree = {}
    primes_found = {2, 3}

    root = (2, 3)
    tree[root] = (0, None)
    queue = [(root, 0)]

    while queue and len(tree) < max_nodes:
        (lo, hi), depth = queue.pop(0)
        if depth >= max_depth:
            continue

        med_target = lo + hi
        med = med_target if isprime(med_target) else nextprime(med_target - 1)

        if med not in primes_found and med < 10**12:
            primes_found.add(med)

            left = (lo, med)
            right = (med, hi)

            tree[left] = (depth+1, (lo, hi))
            tree[right] = (depth+1, (lo, hi))
            queue.append((left, depth+1))
            queue.append((right, depth+1))

    return tree, primes_found


def is_ppt(a, b, c):
    """Check if (a,b,c) is a primitive Pythagorean triple (sorted)."""
    if a <= 0 or b <= 0 or c <= 0:
        return False
    s = sorted([a, b, c])
    return s[0]*s[0] + s[1]*s[1] == s[2]*s[2] and gcd(gcd(s[0], s[1]), s[2]) == 1

def normalize(a, b, c):
    """Canonical form: sorted ascending."""
    return tuple(sorted([abs(a), abs(b), abs(c)]))


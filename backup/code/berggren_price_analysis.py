# ── Tree 4: Height-parametrized tree ────────────────────────────────────
# Organize PPTs by their hypotenuse c. For each c, the PPTs form
# children based on c' > c relationships. Not a matrix tree, but
# a natural ordering.

# ── Tree generation functions ───────────────────────────────────────────

def gen_berggren(matrices, root, max_depth, label=""):
    """Generate tree from 3x3 matrices on (a,b,c). Returns dict and by_depth."""
    tree = {}
    by_depth = defaultdict(set)
    root_n = normalize(*root)
    tree[root_n] = (0, None, -1)
    by_depth[0].add(root_n)

    queue = [(np.array(root, dtype=np.int64), root_n, 0)]
    while queue:
        nq = []
        for vec, parent_n, depth in queue:
            if depth >= max_depth:
                continue
            for i, M in enumerate(matrices):
                ch = M @ vec
                a, b, c = int(ch[0]), int(ch[1]), int(ch[2])
                cn = normalize(a, b, c)
                if cn[0] > 0 and is_ppt(*cn) and cn not in tree:
                    tree[cn] = (depth+1, parent_n, i)
                    by_depth[depth+1].add(cn)
                    nq.append((ch, cn, depth+1))
        queue = nq
    return tree, by_depth

def gen_mn_calkin_wilf(max_depth):
    """Generate PPTs via the Calkin-Wilf tree on (m,n) pairs.

    The Calkin-Wilf tree generates ALL positive rationals p/q with gcd(p,q)=1.
    We traverse it as a binary tree and collect only those nodes where
    m > n > 0, gcd(m,n)=1, m-n odd (valid PPT parametrization).

    This gives a genuinely different tree structure because:
    - It is binary (not ternary)
    - PPT-valid nodes appear at IRREGULAR positions in the CW tree
    - Parent-child in CW is NOT the same as parent-child in Berggren
    """
    tree = {}
    by_depth = defaultdict(set)

    # CW tree: root = 1/1 (i.e. (1,1)), children:
    #   left  = m/(m+n)  -> (m, m+n)
    #   right = (m+n)/n  -> (m+n, n)
    # We record CW depth, but only add nodes that give valid PPTs.
    # When a CW node gives a PPT, its "PPT parent" is the nearest
    # ancestor that also gave a PPT.

    # Track: CW node (m, n), CW depth, nearest PPT ancestor triple
    root_mn = (1, 1)  # CW root
    # (1,1): m=n, not valid for PPT (need m > n and m-n odd)

    # BFS on CW tree, filtering for PPT-valid nodes
    cw_queue = [(root_mn, 0, None)]  # (m,n), cw_depth, ppt_parent_triple
    ppt_depth = {}  # triple -> PPT-tree depth

    max_cw_depth = 20  # CW depth limit (exponential, so keep small)

    while cw_queue:
        nq = []
        for (m, n), cw_d, ppt_parent in cw_queue:
            if cw_d >= max_cw_depth:
                continue

            # Check if this (m,n) gives a valid PPT
            current_ppt_parent = ppt_parent
            if m > n > 0 and gcd(m, n) == 1 and (m - n) % 2 == 1:
                a = m*m - n*n
                b = 2*m*n
                c = m*m + n*n
                triple = normalize(a, b, c)
                if is_ppt(*triple) and triple not in tree:
                    if ppt_parent is None:
                        d = 0
                    else:
                        d = ppt_depth.get(ppt_parent, 0) + 1
                    if d <= max_depth:
                        tree[triple] = (d, ppt_parent, -1)
                        by_depth[d].add(triple)
                        ppt_depth[triple] = d
                        current_ppt_parent = triple

            # CW children: left = (m, m+n), right = (m+n, n)
            nq.append(((m, m+n), cw_d+1, current_ppt_parent))
            nq.append(((m+n, n), cw_d+1, current_ppt_parent))

        cw_queue = nq
        # Memory safety: CW BFS is 2^d, cap at 1M queue entries
        if len(nq) > 500000 or len(tree) > 50000:
            break

    return tree, by_depth

def gen_height_tree(max_depth):
    """Generate PPTs organized by hypotenuse height.

    Start from (3,4,5). At each triple (a,b,c), children are the 3 smallest
    PPTs with hypotenuse > c that share a common factor structure.

    Child selection rule:
      - Child 0: smallest PPT (a',b',c') with c' > c and a' > a
      - Child 1: smallest PPT with c' > c and b' > b
      - Child 2: smallest PPT with c' > c and gcd(c', c) > 1 or c' = c + something_special

    This is NOT guaranteed to generate ALL PPTs, but it's a genuinely
    different tree structure for comparison.
    """
    # Pre-generate all PPTs up to a reasonable hypotenuse
    all_ppts = []
    max_c = 5000  # Enough for depth 10
    for m in range(2, isqrt(max_c) + 1):
        for n in range(1, m):
            if (m - n) % 2 == 0:
                continue
            if gcd(m, n) != 1:
                continue
            a = m*m - n*n
            b = 2*m*n
            c = m*m + n*n
            if c <= max_c:
                all_ppts.append(normalize(a, b, c))

    all_ppts.sort(key=lambda t: (t[2], t[0]))  # Sort by hypotenuse, then a
    ppt_set = set(all_ppts)

    # Build adjacency: for each triple, find 3 nearest larger triples
    tree = {}
    by_depth = defaultdict(set)
    root = normalize(3, 4, 5)
    tree[root] = (0, None, -1)
    by_depth[0].add(root)

    # Index by hypotenuse
    by_hyp = defaultdict(list)
    for t in all_ppts:
        by_hyp[t[2]].append(t)
    hyp_values = sorted(by_hyp.keys())

    queue = [(root, 0)]
    while queue:
        nq = []
        for parent, depth in queue:
            if depth >= max_depth:
                continue
            pa, pb, pc = parent

            # Find 3 nearest unvisited PPTs with c' > pc
            candidates = []
            for h in hyp_values:
                if h <= pc:
                    continue
                for t in by_hyp[h]:
                    if t not in tree:
                        candidates.append(t)
                if len(candidates) >= 10:
                    break

            # Pick 3 most "related" by leg similarity
            candidates.sort(key=lambda t: abs(t[0] - pa) + abs(t[1] - pb))
            for i, child in enumerate(candidates[:3]):
                if child not in tree:
                    tree[child] = (depth+1, parent, i)
                    by_depth[depth+1].add(child)
                    nq.append((child, depth+1))
        queue = nq

    return tree, by_depth


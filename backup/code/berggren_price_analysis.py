B_B = np.array([[ 1,  2,  2], [ 2,  1,  2], [ 2,  2,  3]], dtype=np.int64)
B_C = np.array([[-1,  2,  2], [-2,  1,  2], [-2,  2,  3]], dtype=np.int64)

# ── Tree 2: "CAB" Berggren (permuted child ordering) ────────────────────
# Same matrices, but assigned to different branch positions.
# This creates a genuinely different tree structure: the PATH to reach
# any given triple differs, so parent-child relationships differ.
# "CAB" means: branch 0 = C, branch 1 = A, branch 2 = B

# ── Tree 3: (m, n) Calkin-Wilf Tree ─────────────────────────────────────
# Every PPT = (m^2-n^2, 2mn, m^2+n^2) for unique coprime m>n>0, m-n odd.
# The Calkin-Wilf tree generates all positive rationals m/n via:
#   Left: m/(m+n)    i.e. (m, m+n)
#   Right: (m+n)/n   i.e. (m+n, n)
# But for PPTs we need m>n, m-n odd, gcd(m,n)=1.
# Use Stern-Brocot style: three branches from (m,n):
#   Branch 0: (2m-n, m)   -- Berggren A in (m,n) coords
#   Branch 1: (2m+n, m)   -- Berggren B in (m,n) coords
#   Branch 2: (m+2n, n)   -- Berggren C in (m,n) coords
# This is IDENTICAL to Berggren (proven in v1).
#
# For a DIFFERENT tree, use alternative (m,n) branching:
#   Branch 0: (m+2n, n)        -- same as Berggren C
#   Branch 1: (2m+n, n+m)      -- new: if gcd(2m+n, n+m)=1 and parity OK
#   Branch 2: (3m-2n, 2m-n)    -- new: preserves coprimality
# But these may not generate ALL PPTs. Let's verify.

# Instead, use a well-known DIFFERENT tree: the F_c (Fibonacci-type) tree.
# From each (m,n), generate children via matrices on the (m,n) space
# that are NOT the Berggren matrices.
#
# Key mathematical fact (Romik 2008): The group of automorphisms of the
# Pythagorean equation a^2+b^2=c^2 is generated by the 3 Berggren matrices.
# So ANY tree using 3x3 integer matrices on (a,b,c) that generates all PPTs
# MUST use exactly the Berggren matrices (up to sign/permutation).
#
# However, we CAN get different trees by:
# (a) Permuting which matrix is "child 0", "child 1", "child 2"
# (b) Using the INVERSE direction (parent-finding instead of child-finding)
# (c) Using a completely different parametrization

# ── Tree 3: Inverse Berggren ────────────────────────────────────────────
# Instead of going root->leaves, start from a triple and find which
# Berggren matrix maps it BACK to its parent. The inverse matrices are:
# Computed via numpy inverse, rounded to integer (all dets are +/-1)

def encode_factoring_target(N: int) -> Dict:
    """Given N = p*q (odd composite), find the target Pythagorean triple(s).
    
    Method: N as an odd leg means N = m² - n² = (m-n)(m+n).
    For each factorization N = d₁ * d₂ with d₁ < d₂ and d₁ ≡ d₂ (mod 2):
      m = (d₁ + d₂)/2, n = (d₂ - d₁)/2
      Triple: (N, 2mn, m²+n²)
    
    The TARGET stereographic parameter is t = n/m = (d₂-d₁)/(d₁+d₂)
    """
    targets = []
    for d1 in range(1, int(N**0.5) + 1):
        if N % d1 == 0:
            d2 = N // d1
            if d1 < d2 and (d1 % 2) == (d2 % 2):
                m = (d1 + d2) // 2
                n = (d2 - d1) // 2
                from math import gcd
                g = gcd(m, n)
                if g == 1 and (m - n) % 2 == 1:  # primitive
                    a = m*m - n*n
                    b = 2*m*n
                    c = m*m + n*n
                    t_target = n / m
                    targets.append({
                        'N': N,
                        'factors': (d1, d2),
                        'm': m, 'n': n,
                        'triple': (a, b, c),
                        't_target': t_target,
                        'angle': np.degrees(np.arctan2(b, a)),
                    })
    return targets

def landscape_guided_search(N: int, max_depth: int = 20, verbose: bool = True) -> Dict:
    """
    Search the Berggren tree for a triple whose odd leg divides N,
    using landscape comparison as a heuristic.
    
    HEURISTIC: At each node, compute the stereographic parameter t for each child.
    Compare to the target parameter t_target = n/m where N = (m-n)(m+n).
    Descend to the child whose t is closest to t_target.
    
    The landscape comparison uses angular distance on S¹, which is the
    natural metric for comparing Pythagorean triples.
    """
    targets = encode_factoring_target(N)
    if not targets:
        return {'success': False, 'reason': 'No valid target triples found'}
    
    # Use the first target (smallest factor pair)
    target = targets[0]
    t_target = target['t_target']
    target_angle = np.radians(target['angle'])
    
    if verbose:
        print(f"\n{'='*70}")
        print(f"LANDSCAPE-GUIDED FACTORING: N = {N}")
        print(f"Target: t* = {t_target:.10f} (angle = {target['angle']:.4f}°)")
        print(f"Target triple: {target['triple']}")
        print(f"Target factors: {N} = {target['factors'][0]} × {target['factors'][1]}")
        print(f"{'='*70}")
    
    # Start at root
    a, b, c = 3, 4, 5
    path = []
    visited = [(a, b, c)]
    
    for depth in range(max_depth):
        L_current = Landscape(a, b, c)
        
        # Check if current node gives a factorization
        odd_leg = a if a % 2 == 1 else b
        if odd_leg > 1 and N % odd_leg == 0 and odd_leg < N:
            factor = odd_leg
            cofactor = N // factor
            if verbose:
                print(f"\n✅ FOUND at depth {depth}: {N} = {factor} × {cofactor}")
                print(f"   Triple: ({a}, {b}, {c})")
                print(f"   Path: {''.join(path)}")
            return {
                'success': True,
                'depth': depth,
                'triple': (a, b, c),
                'factor': factor,
                'cofactor': cofactor,
                'path': ''.join(path),
            }
        
        # Generate children landscapes
        children = berggren_children(a, b, c)
        child_landscapes = []
        for i, (ca, cb, cc) in enumerate(children):
            cL = Landscape(ca, cb, cc)
            # Distance to target
            ang_dist = abs(cL.angle - target_angle)
            ang_dist = min(ang_dist, 2*np.pi - ang_dist)
            stereo_dist = abs(cL.t - t_target)
            
            child_landscapes.append({
                'index': i,
                'triple': (ca, cb, cc),
                'angle': cL.angle,
                'stereo_param': cL.t,
                'angular_dist': ang_dist,
                'stereo_dist': stereo_dist,
                'conformal': cL.conformal,
            })
        
        # HEURISTIC: Choose the child with smallest angular distance to target
        best = min(child_landscapes, key=lambda x: x['angular_dist'])
        
        if verbose:
            print(f"\nDepth {depth}: ({a},{b},{c}), t={L_current.t:.6f}, θ={np.degrees(L_current.angle):.2f}°")
            for cl in child_landscapes:
                marker = " ← BEST" if cl['index'] == best['index'] else ""
                branch = ['L','M','R'][cl['index']]
                print(f"  {branch}: {cl['triple']}, t={cl['stereo_param']:.6f}, "
                      f"Δθ={np.degrees(cl['angular_dist']):.4f}°, "
                      f"Δt={cl['stereo_dist']:.8f}{marker}")
        
        a, b, c = best['triple']
        path.append(['L','M','R'][best['index']])
        visited.append((a, b, c))
    
    return {'success': False, 'reason': 'Max depth reached', 'path': ''.join(path)}


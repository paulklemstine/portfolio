def compute_landscape_signature(a: int, b: int, c: int) -> Dict:
    """Compute a rich signature for navigation heuristics.
    
    The signature includes:
    - Stereographic parameter t and its continued fraction expansion
    - Conformal factor and its logarithm
    - Angle on S¹ and angular velocity (rate of change per tree step)
    - Hyperbolic distance from root
    - "Factoring energy" — how close the odd leg is to being highly composite
    """
    L = Landscape(a, b, c)
    
    # Continued fraction expansion of t
    def continued_fraction(x, max_terms=10):
        cf = []
        for _ in range(max_terms):
            if abs(x - round(x)) < 1e-12:
                cf.append(int(round(x)))
                break
            cf.append(int(x))
            x = 1.0 / (x - int(x))
        return cf
    
    cf = continued_fraction(L.t) if L.t > 0 else [0]
    
    # Factoring energy: sum of squared differences from nearest primes
    odd_leg = a if a % 2 == 1 else b
    
    # Number of divisors of odd leg (rough measure of factoring utility)
    def count_divisors(n):
        n = abs(n)
        if n == 0:
            return 0
        count = 0
        for d in range(1, int(n**0.5) + 1):
            if n % d == 0:
                count += 2 if d * d != n else 1
        return count
    
    return {
        'triple': (a, b, c),
        't': L.t,
        'cf': cf,
        'conformal': L.conformal,
        'log_conformal': np.log(L.conformal) if L.conformal > 0 else float('-inf'),
        'angle': L.angle,
        'euclid': (L.m, L.n),
        'odd_leg': odd_leg,
        'odd_leg_divisors': count_divisors(odd_leg),
        'hyp_dist_from_root': abs(np.log(L.t / stereo_param_from_triple(3,4,5))) if L.t > 0 else float('inf'),
    }

def multi_metric_search(N: int, max_depth: int = 25, verbose: bool = True) -> Dict:
    """
    Enhanced search using multiple landscape metrics simultaneously.
    
    Combines:
    1. Angular distance (primary)
    2. Stereographic distance
    3. Conformal factor matching
    4. Continued fraction prefix matching
    
    Weighted combination to select the best child at each step.
    """
    targets = encode_factoring_target(N)
    if not targets:
        return {'success': False}
    
    target = targets[0]
    t_target = target['t_target']
    target_angle = np.radians(target['angle'])
    target_sig = compute_landscape_signature(*target['triple'])
    target_cf = target_sig['cf']
    
    if verbose:
        print(f"\n{'='*70}")
        print(f"MULTI-METRIC LANDSCAPE SEARCH: N = {N}")
        print(f"Target: t* = {t_target:.10f}")
        print(f"Target CF: {target_cf}")
        print(f"{'='*70}")
    
    a, b, c = 3, 4, 5
    path = []
    
    for depth in range(max_depth):
        # Check current node
        odd_leg = a if a % 2 == 1 else b
        if odd_leg > 1 and N % odd_leg == 0 and odd_leg < N:
            if verbose:
                print(f"\n✅ FOUND at depth {depth}: {N} = {odd_leg} × {N//odd_leg}")
            return {'success': True, 'depth': depth, 'path': ''.join(path),
                    'factor': odd_leg}
        
        children = berggren_children(a, b, c)
        
        scores = []
        for i, (ca, cb, cc) in enumerate(children):
            cL = Landscape(ca, cb, cc)
            sig = compute_landscape_signature(ca, cb, cc)
            
            # Metric 1: Angular distance
            ang_dist = abs(cL.angle - target_angle)
            ang_dist = min(ang_dist, 2*np.pi - ang_dist)
            
            # Metric 2: Stereographic distance
            stereo_dist = abs(cL.t - t_target)
            
            # Metric 3: Continued fraction prefix match length
            cf = sig['cf']
            cf_match = 0
            for j in range(min(len(cf), len(target_cf))):
                if cf[j] == target_cf[j]:
                    cf_match += 1
                else:
                    break
            
            # Metric 4: Log-conformal distance
            log_conf_dist = abs(sig['log_conformal'] - target_sig['log_conformal'])
            
            # Combined score (lower is better)
            score = (0.4 * ang_dist + 
                     0.3 * stereo_dist + 
                     0.2 * (10.0 / (cf_match + 1)) +
                     0.1 * log_conf_dist)
            
            scores.append((score, i))
        
        best_score, best_idx = min(scores)
        
        if verbose and depth < 10:
            print(f"  d={depth}: ({a},{b},{c}) → {['L','M','R'][best_idx]} (score={best_score:.6f})")
        
        a, b, c = children[best_idx]
        path.append(['L','M','R'][best_idx])
    
    return {'success': False, 'path': ''.join(path)}


"""
LANDSCAPE ENGINE: Pythagorean Triple Tree × Inverse Stereographic Projection

Research Program: Using geometric "landscapes" generated by inverse stereographic
projection to guide heuristic search through the Berggren tree for integer factoring.

Core Idea:
  Every primitive Pythagorean triple (a,b,c) maps to a rational point (a/c, b/c) on S¹.
  Under stereographic projection, this corresponds to a parameter t = a/(b+c).
  The "landscape" is the local geometry of S¹ near this point, including:
    - Conformal factor (how distances are distorted)
    - Angular position (where on the circle)
    - Curvature of the energy surface for factoring
    - Relationship between parent/child landscapes

The Berggren tree generates ALL primitive Pythagorean triples. If N = p*q,
then N appears as the odd leg of some primitive triple (or a multiple thereof),
and finding that triple gives the factorization.
"""

import numpy as np

class Landscape:
    """The stereographic landscape of a Pythagorean triple.
    
    A "landscape" captures the geometric structure around a triple's
    position on S¹ under inverse stereographic projection.
    """
    def __init__(self, a: int, b: int, c: int):
        self.a, self.b, self.c = a, b, c
        self.t = stereo_param_from_triple(a, b, c)
        self.t_exact = stereo_param_rational(a, b, c)
        self.circle_point = (a/c, b/c)
        self.angle = np.arctan2(b, a)  # angle on circle
        self.conformal = conformal_factor(self.t)
        
        # Euclid parameters
        self.m, self.n = euclid_params(a, b, c)
        
        # Factoring information from the odd leg
        odd_leg = a if a % 2 == 1 else b
        if self.m > 0 and self.n > 0:
            self.factor1 = self.m - self.n  # (m-n)
            self.factor2 = self.m + self.n  # (m+n)
            self.factored_value = self.factor1 * self.factor2  # = odd leg
        else:
            self.factor1 = self.factor2 = self.factored_value = 0
        
        # Energy surface: E(s) = (target - σ(s))² evaluated near t
        # Local curvature of the energy surface
        self.energy_curvature = self._compute_curvature()
    
    def _compute_curvature(self) -> float:
        """Compute the curvature of the stereographic image curve at parameter t."""
        # For σ(t), the curvature κ of the circle at the image point is always 1
        # (it's a unit circle). But the "effective curvature" in the t-parameter
        # is κ_eff = κ * |σ'(t)| = conformal_factor(t)
        return self.conformal
    
    def local_density(self, window: float = 0.1) -> float:
        """How many rational points with small denominator are near this t."""
        count = 0
        for p in range(1, 50):
            for q in range(1, 50):
                if abs(Fraction(p, q) - self.t_exact) < Fraction(1, 100):
                    count += 1
        return count
    
    def distance_to(self, other: 'Landscape') -> Dict[str, float]:
        """Multiple distance metrics between two landscapes."""
        # Angular distance on S¹
        ang_dist = abs(self.angle - other.angle)
        ang_dist = min(ang_dist, 2*np.pi - ang_dist)
        
        # Stereographic distance (on ℝ)
        stereo_dist = abs(self.t - other.t)
        
        # Conformal ratio (how different the local geometries are)
        conf_ratio = self.conformal / other.conformal if other.conformal > 0 else float('inf')
        
        # Hyperbolic distance (using the Poincaré model via Cayley transform)
        # d_hyp = |log(t₁/t₂)| when both t > 0
        if self.t > 0 and other.t > 0:
            hyp_dist = abs(np.log(self.t / other.t))
        else:
            hyp_dist = float('inf')
        
        return {
            'angular': ang_dist,
            'stereographic': stereo_dist,
            'conformal_ratio': conf_ratio,
            'hyperbolic': hyp_dist
        }
    
    def __repr__(self):
        return (f"Landscape(({self.a},{self.b},{self.c}), t={float(self.t_exact):.6f}, "
                f"θ={np.degrees(self.angle):.2f}°, λ={self.conformal:.6f}, "
                f"Euclid=({self.m},{self.n}))")

def generate_landscape_report(a: int, b: int, c: int, depth: int = 0) -> Dict:
    """Generate a comprehensive landscape report for a triple."""
    L = Landscape(a, b, c)
    
    report = {
        'triple': (a, b, c),
        'depth': depth,
        'stereo_param': float(L.t_exact),
        'angle_deg': np.degrees(L.angle),
        'conformal_factor': L.conformal,
        'circle_point': L.circle_point,
        'euclid_params': (L.m, L.n),
        'factorization': f"{L.factored_value} = {L.factor1} × {L.factor2}" if L.factor1 > 0 else "N/A",
    }
    
    # Children landscapes
    children = berggren_children(a, b, c)
    child_landscapes = []
    for i, (ca, cb, cc) in enumerate(children):
        cL = Landscape(ca, cb, cc)
        child_landscapes.append({
            'branch': ['M₁ (Left)', 'M₂ (Mid)', 'M₃ (Right)'][i],
            'triple': (ca, cb, cc),
            'stereo_param': float(cL.t_exact),
            'angle_deg': np.degrees(cL.angle),
            'conformal_factor': cL.conformal,
            'distance_from_parent': L.distance_to(cL),
            'euclid_params': (cL.m, cL.n),
            'factorization': f"{cL.factored_value} = {cL.factor1} × {cL.factor2}" if cL.factor1 > 0 else "N/A",
        })
    report['children'] = child_landscapes
    
    return report


def berggren_children(a: int, b: int, c: int) -> List[Tuple[int, int, int]]:
    """Generate the three children of (a,b,c) in the Berggren tree.
    M₁: (a - 2b + 2c, 2a - b + 2c, 2a - 2b + 3c)
    M₂: (a + 2b + 2c, 2a + b + 2c, 2a + 2b + 3c)
    M₃: (-a + 2b + 2c, -2a + b + 2c, -2a + 2b + 3c)
    """
    c1 = (a - 2*b + 2*c, 2*a - b + 2*c, 2*a - 2*b + 3*c)
    c2 = (a + 2*b + 2*c, 2*a + b + 2*c, 2*a + 2*b + 3*c)
    c3 = (-a + 2*b + 2*c, -2*a + b + 2*c, -2*a + 2*b + 3*c)
    return [c1, c2, c3]

def berggren_parent(a: int, b: int, c: int) -> Optional[Tuple[int, int, int]]:
    """Find the parent of (a,b,c) in the Berggren tree.
    Try all three inverse matrices and return the one giving positive components.
    B₁⁻¹: (a + 2b - 2c, -2a - b + 2c, -2a - 2b + 3c)
    B₂⁻¹: (a + 2b - 2c, 2a + b - 2c, -2a - 2b + 3c)  -- note: same first component
    B₃⁻¹: (-a - 2b + 2c, 2a + b - 2c, -2a - 2b + 3c)
    """
    candidates = [
        (a + 2*b - 2*c, -2*a - b + 2*c, -2*a - 2*b + 3*c),  # B₁⁻¹
        (a + 2*b - 2*c, 2*a + b - 2*c, -2*a - 2*b + 3*c),    # B₂⁻¹
        (-a - 2*b + 2*c, 2*a + b - 2*c, -2*a - 2*b + 3*c),   # B₃⁻¹
    ]
    for pa, pb, pc in candidates:
        if pa > 0 and pb > 0 and pc > 0 and pa*pa + pb*pb == pc*pc:
            return (pa, pb, pc)
    return None

def tree_path_to_triple(path: str) -> Tuple[int, int, int]:
    """Convert a path string like 'LMR' to a Berggren triple.
    L = left (M₁), M = mid (M₂), R = right (M₃).
    """
    a, b, c = 3, 4, 5
    for ch in path:
        children = berggren_children(a, b, c)
        if ch == 'L':
            a, b, c = children[0]
        elif ch == 'M':
            a, b, c = children[1]
        elif ch == 'R':
            a, b, c = children[2]
    return (a, b, c)

def triple_to_path(a: int, b: int, c: int) -> str:
    """Find the Berggren tree path from root to (a,b,c)."""
    path = []
    while (a, b, c) != (3, 4, 5):
        parent = berggren_parent(a, b, c)
        if parent is None:
            return "NOT_FOUND"
        pa, pb, pc = parent
        children = berggren_children(pa, pb, pc)
        if (a, b, c) == children[0]:
            path.append('L')
        elif (a, b, c) == children[1]:
            path.append('M')
        elif (a, b, c) == children[2]:
            path.append('R')
        else:
            return "ERROR"
        a, b, c = pa, pb, pc
    return ''.join(reversed(path))


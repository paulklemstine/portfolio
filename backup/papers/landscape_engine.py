def analyze_landscape_transitions(max_depth: int = 3):
    """Analyze how landscapes transform from parent to child.
    
    Key questions:
    1. How does the stereographic parameter t change across generations?
    2. Is there a pattern in the conformal factor ratios?
    3. How do the angular separations between children vary?
    4. Can we predict which subtree contains a target based on the parent landscape?
    """
    print("\n" + "="*70)
    print("LANDSCAPE TRANSITION ANALYSIS")
    print("="*70)
    
    # Generate tree to specified depth
    def explore(a, b, c, depth, path=""):
        L = Landscape(a, b, c)
        children = berggren_children(a, b, c)
        
        print(f"\n{'  '*depth}Node: ({a},{b},{c}) [path={path or 'root'}]")
        print(f"{'  '*depth}  t = {L.t:.8f}, θ = {np.degrees(L.angle):.4f}°, λ = {L.conformal:.8f}")
        print(f"{'  '*depth}  Euclid: m={L.m}, n={L.n}")
        if L.factor1 > 0:
            print(f"{'  '*depth}  Factorization: {L.factored_value} = {L.factor1} × {L.factor2}")
        
        if depth < max_depth:
            child_params = []
            for i, (ca, cb, cc) in enumerate(children):
                branch = ['L','M','R'][i]
                cL = Landscape(ca, cb, cc)
                child_params.append(cL.t)
                
                # Analyze the transition
                t_ratio = cL.t / L.t if L.t != 0 else float('inf')
                conf_ratio = cL.conformal / L.conformal
                ang_shift = np.degrees(cL.angle - L.angle)
                
                print(f"{'  '*depth}  → {branch}: ({ca},{cb},{cc})")
                print(f"{'  '*depth}      t_ratio={t_ratio:.6f}, conf_ratio={conf_ratio:.8f}, Δθ={ang_shift:.4f}°")
                
                explore(ca, cb, cc, depth + 1, path + branch)
            
            # Angular separation between children
            if len(child_params) == 3:
                print(f"{'  '*depth}  Children t-spread: [{min(child_params):.6f}, {max(child_params):.6f}]")
                print(f"{'  '*depth}  Children t-range: {max(child_params) - min(child_params):.6f}")
    
    explore(3, 4, 5, 0)


# ============================================================
# §1: CORE MATHEMATICAL OBJECTS
# ============================================================

def inv_stereo(t: float) -> Tuple[float, float]:
    """Inverse stereographic projection: ℝ → S¹
    σ(t) = (2t/(1+t²), (1-t²)/(1+t²))
    """
    d = 1 + t**2
    return (2*t/d, (1 - t**2)/d)

def stereo_forward(x: float, y: float) -> float:
    """Forward stereographic projection: S¹ → ℝ
    Given (x,y) on S¹ with y ≠ -1, returns t = x/(1+y)
    """
    if abs(1 + y) < 1e-15:
        return float('inf')
    return x / (1 + y)

def conformal_factor(t: float) -> float:
    """The conformal factor of inverse stereographic projection at parameter t.
    λ(t) = 2/(1+t²)
    This measures how much distances are stretched/compressed.
    """
    return 2.0 / (1 + t**2)

def euclid_params(a: int, b: int, c: int) -> Tuple[int, int]:
    """Extract Euclid parameters (m,n) from a primitive triple (a,b,c).
    If a is odd: a = m²-n², b = 2mn, c = m²+n²
    So m² = (a+c)/2, n² = (c-a)/2, and m,n can be recovered.
    If b is odd, swap a,b first.
    """
    if a % 2 == 0:
        a, b = b, a
    m_sq = (a + c) // 2
    n_sq = (c - a) // 2
    m = int(round(m_sq**0.5))
    n = int(round(n_sq**0.5))
    if m*m == m_sq and n*n == n_sq:
        return (m, n)
    return (0, 0)  # Not a valid triple or not primitive

def stereo_param_from_triple(a: int, b: int, c: int) -> float:
    """Get stereographic parameter t for a Pythagorean triple.
    The point on S¹ is (a/c, b/c), so t = (a/c)/(1 + b/c) = a/(c+b).
    Alternatively, if we use Euclid params: t = n/m.
    """
    if c + b == 0:
        return float('inf')
    return a / (c + b)

def stereo_param_rational(a: int, b: int, c: int) -> Fraction:
    """Exact rational stereographic parameter."""
    return Fraction(a, c + b)

